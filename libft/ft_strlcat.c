#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	dest_l;

	dest_l = ft_strlen(dest);
	if (size <= dest_l)
		return (ft_strlen(src) + size);
	i = 0;
	j = dest_l;
	while (src[i] != '\0' && j < size - 1)
	{
		dest[j] = src[i];
		i++;
		j++;
	}
	dest[j] = '\0';
	return (dest_l + ft_strlen(src));
}
